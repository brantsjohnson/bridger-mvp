// Simple mapping for quick emoji lookup
export const hobbyToEmoji: { [key: string]: string } = {};

// Populate the simple mapping
Object.values(hobbyCategories).forEach(category => {
  Object.entries(category.hobbies).forEach(([hobby, emoji]) => {
    hobbyToEmoji[hobby] = emoji;
  });
});

// Helper function to get emoji for a hobby
export const getHobbyEmoji = (hobby: string): string => {
  return hobbyToEmoji[hobby] || "âœ¨";
};

// Helper function to get categorized hobbies for profile page
export const getCategorizedHobbies = (hobbies: string[]) => {
  const categorized: { [categoryName: string]: { emoji: string; hobbies: { name: string; emoji: string }[] } } = {};
  
  hobbies.forEach(hobby => {
    let found = false;
    Object.entries(hobbyCategories).forEach(([categoryName, category]) => {
      if (category.hobbies[hobby]) {
        if (!categorized[categoryName]) {
          categorized[categoryName] = {
            emoji: category.emoji,
            hobbies: []
          };
        }
        categorized[categoryName].hobbies.push({
          name: hobby,
          emoji: category.hobbies[hobby]
        });
        found = true;
      }
    });
    
    // If hobby not found in any category, add to "Custom" category
    if (!found) {
      if (!categorized["Custom"]) {
        categorized["Custom"] = {
          emoji: "âœ¨",
          hobbies: []
        };
      }
      categorized["Custom"].hobbies.push({
        name: hobby,
        emoji: "âœ¨"
      });
    }
  });
  
  return categorized;
};

// Helper function to get all available hobbies
export const getAllHobbies = () => {
  const allHobbies: { [categoryName: string]: { emoji: string; hobbies: string[] } } = {};
  
  Object.entries(hobbyCategories).forEach(([categoryName, category]) => {
    allHobbies[categoryName] = {
      emoji: category.emoji,
      hobbies: Object.keys(category.hobbies)
    };
  });
  
  return allHobbies;
};